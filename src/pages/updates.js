import React from 'react'
import { graphql } from 'gatsby'
import get from 'lodash/get'
import { Helmet } from 'react-helmet'
import Layout from '../components/layout'
import ArticlePreview from '../components/article-preview'
import { ChakraProvider, Text, Box, Button, Center, Hstack } from '@chakra-ui/react'

class BlogIndex extends React.Component {
  render() {
    const siteTitle = get(this, 'props.data.site.siteMetadata.title')
    const posts = get(this, 'props.data.allContentfulBlogPost.edges')

    return (
      <ChakraProvider>
      <Layout>
          <Helmet title={siteTitle} />
          <Box m={10} mx={20}>
            <Text fontSize="3xl" fontWeight="bold"> Meeting Updates </Text>
            <Text>
              Meeting slides and updates will be posted here after our club meetings biweekly on Wednesdays. If you have any questions, don't hesitate to contact us!
            </Text>
            <br />
            <ul className="article-list">
              {posts.map(({ node }) => {
                return (
                  <li key={node.slug}>
                    <ArticlePreview article={node} />
                  </li>
                )
              })}
            </ul>
            </Box>
        
      </Layout>
      </ChakraProvider>
    )
  }
}

export default BlogIndex

export const pageQuery = graphql`
  query BlogIndexQuery {
    allContentfulBlogPost(sort: { fields: [publishDate], order: DESC }) {
      edges {
        node {
          title
          slug
          publishDate(formatString: "MMMM Do, YYYY")
          tags
          heroImage {
            fluid(maxWidth: 350, maxHeight: 196, resizingBehavior: SCALE) {
              ...GatsbyContentfulFluid_tracedSVG
            }
          }
          description {
            childMarkdownRemark {
              html
            }
          }
        }
      }
    }
  }
`
